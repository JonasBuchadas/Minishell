#include "minishell.h"

void	bt_env(void)
{
	int	c;
	int i;

	c = -1;
	while (ms()->envp[++c])
	{
		i = 0;
		while (ms()->envp[c][i] && ms()->envp[c][i] != '=')
			i++;
		if (ms()->envp[c][++i] != '\0')
		{
			ft_putstr_fd(ms()->envp[c], 1);
			write(1, "\n", 1);
		}
	}
}
