#include "minishell.h"

void	bt_env(void)
{
	int	c;

	c = -1;
	while (ms()->envp[++c])
	{
		ft_putstr_fd(ms()->envp[c], 1);
		write(1, "\n", 1);
	}
	exit(EXIT_SUCCESS);
}
